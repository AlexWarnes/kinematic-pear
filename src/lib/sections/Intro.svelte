<h2>
Introduction to Joints
</h2>

<p>Joints are constraints placed on the movement of one thing relative to another. The physical world is filled with joints: doors, shoulders, knees, wheels, umbrellas, etc.</p>

<p>Think about the differences between each of those, and specifically about <em>how</em> different parts can move. Can it rotate? Does it have limits to distance or direction?</p>

<p>There is a taxonomy to joints based on the way things can/cannot move relative to each other. Here we'll cover four joint types:</p>

<ul>
  <li>Fixed</li>
  <li>Prismatic</li>
  <li>Revolute</li>
  <li>Spherical</li>
</ul>

